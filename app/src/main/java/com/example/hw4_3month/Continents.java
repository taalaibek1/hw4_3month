package com.example.hw4_3month;

public class Continents {
    private String name;
    private String flag;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }



    public Continents(String name, String flag) {
        this.name = name;
        this.flag = flag;
    }
}
